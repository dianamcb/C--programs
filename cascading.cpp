// Program to explain Cascading or Insertion ( << )
// operator and Cascading of Extraction ( >> ) operator

#include <iostream>

int main()
{
            int num1, num2, sum;

             cout << "\n Enter two numbers ::: ";
             cin >> num1 >> num2;

             sum = num1 + num2;

             cout << "\n" << num1 << "+" << num2 << "=" << sum;

return 0;
}
